# CALCULATING THE TOTAL AMT OF WATER IN A POND
r = 84
pi = 3.14
water = 1.4
area = pi * r ** 2
total_water = area * water
print("Total amount of water in the pond:", int(total_water), "liters")#int is used to convert float into int.

#-----------------------output-----------------------
#Total amount of water in the pond: 31018 liters